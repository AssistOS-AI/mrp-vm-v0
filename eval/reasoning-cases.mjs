const CASE_MAP = new Map(REASONING_DEMO_CASES.map((entry) => [entry.id, entry]));

export function listChatDemoTasks() {
  return REASONING_DEMO_CASES
    .map((entry) => ({
      order: entry.order,
      id: entry.id,
      title: entry.title,
      summary: entry.summary,
      reasoning_classes: [...entry.reasoning_classes],
      prompt: entry.prompt,
    }))
    .sort((left, right) => left.order - right.order);
}

export function getReasoningDemoCase(caseId) {
  return CASE_MAP.get(caseId) ?? null;
}

export function validateReasoningCaseOutput(caseOrId, text) {
  const caseDef = typeof caseOrId === 'string' ? getReasoningDemoCase(caseOrId) : caseOrId;
  if (!caseDef) {
    return {
      ok: false,
      failures: ['Unknown reasoning case.'],
    };
  }
  const value = String(text ?? '');
  const failures = [];
  for (const check of caseDef.validation ?? []) {
    check.pattern.lastIndex = 0;
    if (!check.pattern.test(value)) {
      failures.push(check.message);
    }
  }
  return {
    ok: failures.length === 0,
    failures,
  };
}
