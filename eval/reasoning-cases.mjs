function makeValidation(pattern, message) {
  return { pattern, message };
}

function escapeRegex(text) {
  return String(text).replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function makeSectionValidations(sectionNames = []) {
  return sectionNames.map((name) => makeValidation(new RegExp(`${escapeRegex(name)}:`, 'i'), `Missing ${name} section.`));
}

